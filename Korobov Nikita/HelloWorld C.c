#include <stdio.h>  //Подключаем стандартную библиотеку команд С

int main() //Точка входа в программу на языке С, int - указывает, что функция main возвращает целочисленное значение (при успешном завершении main вернет 0)
{
    printf("Hello World!\n"); //Выводим текст на экран, \n переводит курсор на новую строку
    getchar(); // Чтобы консоль не закрылась ждем ввод любого значения

    return 0; // Условный оператор, который возращает 0 при успешной работе программы до этого момента
}